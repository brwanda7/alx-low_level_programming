#include <stdlib.h>
#include <time.h>
#include <stdio.h>
/**
 * main - how to know if number is negative , positive or zero using if
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	int a;

	for (a = 48; a < 58; a++)
	{
		putchar(a);
	}
	for (a = 97; a < 103; a++)
	{
		putchar(a);
	}
	putchar('\n');
	return (0);
}

